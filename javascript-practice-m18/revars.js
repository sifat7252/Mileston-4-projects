// simple while loop bebohar kore 0 - 10 porjonto dekhano 

var num = 0; 
while( num <= 10){
    console.log(num);
    num++;
}

// while loop bebohar kore revers 10- 1 porjonto dekhano 

var num = 10; 
while( num >= 1){
    console.log(num);
    num--;
}

// simple for loop bebohhar kore 0- 10 porjonto dekhano 

for(var i = 0; i <= 10; i++){
    console.log(i)
}

// for loop bebohar kore 10-1 revers loop dekhano 

for(var i= 10; i >= 1; i--){
    console.log(i);
}